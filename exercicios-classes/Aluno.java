public class Aluno {
    long matricula;
    String nome;
    int idade;
    String curso;
    float mediaGeral;

    public Aluno(long matricula, String nome, int idade, String curso, float mediaGeral){
        this.matricula = matricula;
        this.nome = nome;
        this.idade = idade;
        this.curso = curso;
        this.mediaGeral = mediaGeral;
    }
}
